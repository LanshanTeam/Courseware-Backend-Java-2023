public class DataType {
    // 演示数据类型
    public static void main(String[] args) {
        int e = 1;
        // 下面是整数类型
        byte a = 1;
        short b = 2;
        int c = 4;
        long d = 8;
        System.out.println(Integer.MAX_VALUE);
        long l1 = 2147483648L;
        // 小数(浮点数)
        // float
        float f1 = 2.4F;
        double d1 = 2.4;
        System.out.println("f1=" + f1);
        System.out.println("d1=" + d1);
        // 字符型 'A', 'W' , '=' ,','
        char c1 = 'A';
        // 布尔值 boolean
        boolean b1 = 3 > 2;
        System.out.println("b1=" + b1);
    }
}
