public class AssignmentOperator {
    public static void main(String[] args) {
        // 演示赋值运算符
        // +=
        int a = 20;
        a += 10; // a = a+10;
        System.out.println(a);
        a -= 10; // a = a -10;
        System.out.println(a);
        // *=
        int a1 = 10;
        a1 *= 30; // a1 = a1 * 30;
        System.out.println(a1);
        a1 /= 30; // a1 = a1 / 30;
        System.out.println(a1);
    }
}
