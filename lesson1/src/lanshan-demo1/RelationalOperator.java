public class RelationalOperator {
    public static void main(String[] args) {
        // 演示关系运算符
        double d1 = 20;
        double d2 = 30;
        double d3 = 21;
        double d4 = 33;
        boolean b1 = d1 > d2;
        boolean b2 = d1 < d2;
        System.out.println(d1 == d2);
        System.out.println(d1 != d2);
    }
}
